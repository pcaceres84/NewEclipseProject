package arrayListDemo;

public class switchDemo2 {

	public static void main(String[] args) {
		
		int students= 5;
		
		String studentsString;
		switch(students) {
		case 1:studentsString="John";
		break;
		case 2:studentsString="Mike";
		break;
		case 3:studentsString="David";
		break;
		case 4:studentsString="Tim";
		break;
		case 5:studentsString="Charles";
		break;
		case 6:studentsString="Sam";
		break;
		default:studentsString= "Invalid Name";
		break;
		
		
		
		
		}
		System.out.println(studentsString);

	
	
	}

}
