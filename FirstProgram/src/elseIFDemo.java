
public class elseIFDemo {

	public static void main(String[] args) {
		//else if
		
		int studentScore=89;
		
		if(studentScore!=90) {
			System.out.println("A");
		}else if(studentScore!=80) {
			System.out.println("B");
		}else if(studentScore!=70) {
			System.out.println("C");
		}else {
			System.out.println("F");
			
		}

	}

}
